package j05062;

public class SV {
    private String ten;
    private float gpa;
    private int rl;

    public SV(String ten, float gpa, int rl) {
        this.ten = ten;
        this.gpa = gpa;
        this.rl = rl;
    }

    public String getTen() {
        return ten;
    }

    public float getGpa() {
        return gpa;
    }

    public int getRl() {
        return rl;
    }
    
}
