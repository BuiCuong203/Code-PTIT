package j05070;

public class CLB {
    private String ma;
    private String ten;
    private int ve;

    public CLB(String ma, String ten, int ve) {
        this.ma = ma;
        this.ten = ten;
        this.ve = ve;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }

    public int getVe() {
        return ve;
    }
    
}