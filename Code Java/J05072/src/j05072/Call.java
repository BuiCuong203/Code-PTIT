package j05072;

public class Call {
    private String sdt, start, end;

    public Call(String sdt, String start, String end) {
        this.sdt = sdt;
        this.start = start;
        this.end = end;
    }

    public String getSdt() {
        return sdt;
    }

    public String getStart() {
        return start;
    }

    public String getEnd() {
        return end;
    }
    
}

