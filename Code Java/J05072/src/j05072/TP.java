package j05072;

public class TP {
    private int ma, giacuoc;
    private String tinh;

    public TP(int ma, String tinh, int giacuoc) {
        this.ma = ma;
        this.tinh = tinh;
        this.giacuoc = giacuoc;
    }

    public int getMa() {
        return ma;
    }

    public int getGiacuoc() {
        return giacuoc;
    }

    public String getTinh() {
        return tinh;
    }
    
}

