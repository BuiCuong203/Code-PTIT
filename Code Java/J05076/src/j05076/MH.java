package j05076;

public class MH {
    private String ma, ten;
    private char xl;

    public MH(String ma, String ten, char xl) {
        this.ma = ma;
        this.ten = ten;
        this.xl = xl;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }

    public char getXl() {
        return xl;
    }
    
}
